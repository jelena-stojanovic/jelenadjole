    private void setcbModel(){
        cbAreaOfInterest.removeAllItems();
        List<String> areasOfInterest= FormCoordinator.getInstance().getAreasOfInterest();
        for (String string : areasOfInterest) {
          cbAreaOfInterest.addItem(string);   
        }
    }

