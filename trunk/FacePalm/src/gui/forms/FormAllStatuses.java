private void btnFacePalmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFacePalmActionPerformed
        try {
            int row = tblstatus.getSelectedRow();
            TableModelStatus tms = (TableModelStatus) tblstatus.getModel();
            Status status = tms.getStatus(row);
            status.setNofacePalm(status.getNofacePalm()+1);
            FormCoordinator.getInstance().updateStatus(status);
            refresh();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FormAllStatuses.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PanelStatus.class.getName()).log(Level.SEVERE, null, ex);
        }
    
}//GEN-LAST:event_btnFacePalmActionPerformed

private void btnLikeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLikeActionPerformed
        try {
            int row = tblstatus.getSelectedRow();
            TableModelStatus tms = (TableModelStatus) tblstatus.getModel();
            Status status = tms.getStatus(row);
            status.setNoLike(status.getNoLike()+1);
            FormCoordinator.getInstance().updateStatus(status);
            refresh();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FormAllStatuses.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PanelStatus.class.getName()).log(Level.SEVERE, null, ex);
        }
    
}//GEN-LAST:event_btnLikeActionPerformed

